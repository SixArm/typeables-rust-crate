//! # Metre^2
//!
//! Examples:
//!
//! ```rust
//! # use ::typeables::metre_2::*;
//! let x = Metre2AsStructF64(1.0); // 1 metre square
//! ```
//!
//! The metre^2 is a unit of area.
//!
//! The symbol is m^2.
//!
//! Compare:
//!
//!   * Metre is for length
//!
//!   * Metre^2 is for area
//! 
//!   * Metre^3 is for volume
//!
//!   * Metre Per Second is for speed

pub struct Metre2AsStructI8(pub i8);
pub struct Metre2AsStructI16(pub i16);
pub struct Metre2AsStructI32(pub i32);
pub struct Metre2AsStructI64(pub i64);
pub struct Metre2AsStructI128(pub i128);
pub struct Metre2AsStructISize(pub isize);
pub struct Metre2AsStructU8(pub u8);
pub struct Metre2AsStructU16(pub u16);
pub struct Metre2AsStructU32(pub u32);
pub struct Metre2AsStructU64(pub u64);
pub struct Metre2AsStructU128(pub u128);
pub struct Metre2AsStructUSize(pub usize);
pub struct Metre2AsStructF32(pub f32);
pub struct Metre2AsStructF64(pub f64);

pub type Metre2AsTypeI8 = i8;
pub type Metre2AsTypeI16 = i16;
pub type Metre2AsTypeI32 = i32;
pub type Metre2AsTypeI64 = i64;
pub type Metre2AsTypeI128 = i128;
pub type Metre2AsTypeISize = isize;
pub type Metre2AsTypeU8 = u8;
pub type Metre2AsTypeU16 = u16;
pub type Metre2AsTypeU32 = u32;
pub type Metre2AsTypeU64 = u64;
pub type Metre2AsTypeU128 = u128;
pub type Metre2AsTypeUSize = usize;
pub type Metre2AsTypeF32 = f32;
pub type Metre2AsTypeF64 = f64;
