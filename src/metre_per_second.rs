//! # Metre Per Second
//!
//! Examples:
//!
//! ```rust
//! # use typeables::metre_per_second::*;
//! let x = MetrePerSecondAsStructF64(1.0);
//! ```
//!
//! <https://wikipedia.org/wiki/Metre>
//!
//! The metre per second is a unit of speed.
//!
//! The symbol is m/s.
//!
//! Compare:
//!
//!   * Metre is for length
//!
//!   * Metre^2 is for area
//!
//!   * Metre^3 is for volume
//!
//!   * Metre Per Second is for speed

