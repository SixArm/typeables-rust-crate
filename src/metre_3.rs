//! # Metre^3
//!
//! Examples:
//!
//! ```rust
//! # use ::typeables::metre_3::*;
//! let x = Metre3AsStructF64(1.0); // 1 metre cube
//! ```
//!
//! The metre^3 is a unit of volume.
//!
//! The symbol is m^3.
//!
//! Compare:
//!
//!   * Metre is for length
//!
//!   * Metre^2 is for area
//!
//!   * Metre^3 is for volume
//!
//!   * Metre Per Second is for speed

pub struct Metre3AsStructI8(pub i8);
pub struct Metre3AsStructI16(pub i16);
pub struct Metre3AsStructI32(pub i32);
pub struct Metre3AsStructI64(pub i64);
pub struct Metre3AsStructI128(pub i128);
pub struct Metre3AsStructISize(pub isize);
pub struct Metre3AsStructU8(pub u8);
pub struct Metre3AsStructU16(pub u16);
pub struct Metre3AsStructU32(pub u32);
pub struct Metre3AsStructU64(pub u64);
pub struct Metre3AsStructU128(pub u128);
pub struct Metre3AsStructUSize(pub usize);
pub struct Metre3AsStructF32(pub f32);
pub struct Metre3AsStructF64(pub f64);

pub type Metre3AsTypeI8 = i8;
pub type Metre3AsTypeI16 = i16;
pub type Metre3AsTypeI32 = i32;
pub type Metre3AsTypeI64 = i64;
